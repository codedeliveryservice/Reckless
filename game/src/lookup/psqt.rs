/// Generates a piece-square table indexed by [color][piece][square].
pub fn generate_map() -> [[[i32; 64]; 6]; 2] {
    let mut map = [[[0; 64]; 6]; 2];

    for square in 0..64 {
        for (piece, bonuses) in BONUSES.iter().enumerate() {
            map[Color::Black as usize][piece][square] = -bonuses[square];
            map[Color::White as usize][piece][square ^ 56] = bonuses[square];
        }
    }

    map
}
